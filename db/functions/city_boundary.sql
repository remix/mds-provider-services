                    [-118.49441445322626,34.05061708056851],
                    [-118.48381739066373,34.04155497426178],
                    [-118.47718395348072,34.046624231074354],
                    [-118.45929353126496,34.031312445429656],
                    [-118.45306517868974,34.028040167931216],
                    [-118.44351271585215,34.01656531485521],
                    [-118.48389105452992,33.995146776898125]
                ]
            ]
    }'), 4326) AS city_boundary
$BODY$;
